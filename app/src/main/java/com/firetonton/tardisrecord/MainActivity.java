    private void onFabClicked(View view) {
        int curPos = mViewPager.getCurrentItem();
        Fragment curFragment = mSectionsPagerAdapter.getItem(curPos);

        if (curFragment instanceof IFabClickable) {
            ((IFabClickable) curFragment).onFabClick(view);
        }
    }


